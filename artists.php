				<div class="bio">
					<p>Closely  resembling the timid and elusive creatures they share their name with, <strong>Parliament Of  Owls</strong> would also prefer to sit unnoticed while transmitting gentle,  lo-fi lullabies into the noisy wilderness.</p>
