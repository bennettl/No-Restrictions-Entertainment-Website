				<div class="name">Christine Wu</div>
				<div class="bio">
					<p>Christine's professional career began at age 17. She has since played as a soloist and ensemble member all around the United States, Europe, and the Caribbean. Professional touring has taken her to Carnegie Hall as well as most of the well known concert halls throughout Europe. Christine studied violin at the University of Southern California with Robert Lipsett. During her final semester, she won a prestigious (tenured) full time position with the Houston Symphony Orchestra in 2001. In 2005, she made the unusual decision to resign from the world class symphony orchestra job. She studied record production and once she had mastered Pro Tools, she ran away from home to join the circus aka the Record, Film and Television industries in Los Angeles.</p>
					<p>Newly reinvented, Christine now works with producers, songwriters and bands to compose original string arrangements for rock, pop, country and urban records. She also works with composers, doing strings for film, television, trailers and jingles. Christine also writes original songs and records on several additional instruments, but we digress. Christine has worked on many major records, films and TV shows since returning to Los Angeles in 2007. She has also worked behind the scenes producing and editing vocals for recording sessions, using her perfect pitch, Pro Tools skills and quest for perfection to work with producers and mixers. Christine plays a 1782 Gragnani and a Violectra electric violin. Christine also records and composes on the cello and if pressed, the viola.</p>
