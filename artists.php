			<div id="nf">
				<div class="name">Nima Fakhrara</div>
				<div class="bio">
					<p>Born  in Tehran, Iran, Nima has been with Persian classical music from birth, he  slept to the sound of Music. The Santour was his lullaby. </p>
					<p>At  the tender age of five he started to learn music and The Santour. He started to  study music with Ostad Tavakoli, then with Ostad Hematabadi and finally with  Ostad Saeed Sabet. As the youngest musician in the academy he played in many  gatherings side by side with Ostad Sabet. He finished the study of Radif  (Persian Classical Repertoire) with Ostad Sabet. He traveled to the U.S. to  continue his education and his musical career. 
					</p>
					<p>In  Washington D.C. he studied the art of improvising, composition, and conducting  with Dr. Nader Majd.
					Under  the supervision of Dr. Alireza Analoui he researched the art of complex  rhythms. He composed many pieces for The Chakavak Ensemble, which was conducted  by Dr Majd. He then traveled to Los Angeles to continue his education in Media  Composition at California State University Northridge.
					At  California State University Northridge he has composed and arranged many pieces  for large ensembles. His concert pieces performed by the Studio Ensemble  include, &quot;Magicopollis&quot;, &quot;Heist Suite&quot;, &quot;Prelude of  the World&quot;, and &quot;Kingdom&quot; to name a few. 
					</p>
					<p>He  has also studied Ethnomusicology, Anthropology, and Instrument Making. With the  information gathered he made the instrument Delnavaz. He has composed many  pieces for short films including, <em>Lost Dream</em> and <em>Evolution</em> both Directed by Asif Ahmad, <em>Tumblers</em> Directed by Vinny Delay, and <em>Broadway  Bound</em> directed by Bandar Albuliwi to name a few.</p>
					<p><a href="http://www.nimafilmmusic.com/NFM.html" target="_blank">NimaFilmMusic.com</a></p>
