			<div id="kd">
				<div class="name">Kevin Doucette </div>
				<div class="bio">
					<p>Kevin Doucette is a film, TV and video game music composer in Santa Monica, CA. He began his musical career as a classical and jazz trained pianist before studying at the renowned Berklee College of Music as a George Delerue Film Scoring Scholarship recipient. While teaching film scoring as a Berklee Faculty member, Kevin helped establish Music and Motion Productions <a href="http://www.mumoproductions.com/" target="_blank">(MUMO  Productions)</a> with composer Lucas Vidal in 2005. In 2009 Kevin relocated with the company to Los Angeles and has worked on both domestic and foreign films including the Emmy Nominated "The Lois Wilson Story" and "The Immortal Voyage of Captain Drake". In early 2010, he was approached by A.R. Rahman to join the <a href="http://www.arrahmanlive.com/" target="_blank">Jai  Ho World Tour</a> on which he works alongside an Oscar Winning composer, the world's best Indian artists and LA's A-list musicians.</p>
					<p>Kevin is Canadian and spent most of his childhood in France and Holland, learning to speak both French and Dutch along the way. He began his musical studies as a classical pianist before studying jazz and working in pop music. Kevin began studying composition and music technology while in high school in Holland. Prior to his degree in Film Scoring from Berklee College of Music, Kevin earned degrees in Mechanical Engineering, Music and History from Queens University in Ontario, Canada.</p>
					<p>While at Queen's University Kevin was the captain of the Division One Men's Soccer Varsity team for 3 years, winning both leadership and MVP awards. In his ever dwindling leisure hours, Kevin can be found playing soccer with Valhalla FC in the LA Men's Premiere league, tennis, golf, and squash.</p>
					<p> An avid "techie", Kevin also consults for Steinberg North America specializing in Cubase and Nuendo in the West Coast market, assisting other composers, producers and music libraries with their technology and sample library needs.</p>
